package javapacket;

public class ModulusProgram {
	//Develop a Java program to divide first integer number from second and print the result on the screen

	public static void main(String[] args) {
		
		int a =60;
		int b =50;
		//// Modulus sign will give reminder after division that means if we divide 50/10 it will give reminder 0
		int mod=a%b;
		System.out.println("Print the reminder "+mod);
	}
}
