package javapacket;

public class WhileLoop {
	
	public static void main(String[] args) {
	//How to print Marlene 10 times using  while loop
			
			int a =1;
			// perform the loop operation using while condition
			while(a<=10)
			{
				System.out.println("Marlene");
				// Increase the value by one .The loop works till the condition is false 
				a++;
			}


	}
}